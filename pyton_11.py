# Project 11: String Playground
# Topic: Working with Strings

text = "Python is Fun!"

print("Original:", text)
print("Lower:", text.lower())
print("Upper:", text.upper())
print("Title:", text.title())

# replace
print("Replace:", text.replace("Fun", "Awesome"))

# split
words = text.split()
print("Words:", words)

# length
print("Length:", len(text))
